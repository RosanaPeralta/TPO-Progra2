package org.example.Ejercicio2_5.model.montecarlo;

public class Cordenade {

    private double x;
    private double y;

    public Cordenade(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getx() {
        return x;
    }

    public double gety() {
        return y;
    }
}
